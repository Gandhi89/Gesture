package com.example.shivamgandhi.gesture;

public class User {

    public String Email;
    public String userName;
    public int wining;
    public String title;
    public double lat;
    public double log;
    public String Status;

    public User() {

    }

    public User(String a, String b, int c, String d, double e, double f, String g) {
        Email = a;
        userName = b;
        wining = c;
        title = d;
        lat = e;
        log = f;
        Status = g;
    }

}
